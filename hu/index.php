      <div id="features">
        <h2> jellemzők </h2>
        <p><span id="version">0.9.0-s verzió</span></p>
        <h3> égbolt </h3>
        <ul>
          <li> több mint 600&nbsp;000 csillag az alapkatalógusban </li>
          <li> több mint 210 millió csillag a kiegészítő katalógusokban </li>
          <li> csillagképek vonalrajzai és illusztrációi </li>
          <li> csillagködök képei (teljes Messier-katalógus) </li>
          <li> élethű Tejút </li>
          <li> igazán élethű légkör, napkelte és napnyugta </li>
          <li> a bolygók és holdjaik </li>
        </ul>
        <h3> felhasználói felület </h3>
        <ul>
          <li> hatékony nagyítás és kicsinyítés </li>
          <li> az idő múlásának szabályozása </li>
          <li> többnyelvű felület </li>
          <li> saját bemutatók felvétele és lejátszása parancsfájlok használatával </li>
          <li> halszem vetület planetáriumokhoz </li>
          <li> gömbtükör vetület otthoni kupolához </li>
          <li> billentyűkkel jól irányítható grafikus felület </li>
          <li> távcsövek vezérlése </li>
        </ul>
        <h3> megjelenítés </h3>
        <ul>
          <li> egyenlítői és horizontális rács </li>
          <li> pislákoló csillagok </li>
          <li> hullócsillagok </li>
          <li> fogyatkozások szimulálása </li>
          <li> cserélhető tájképek; mostantól gömbpanorámákkal is </li>
        </ul>
        <h3> testreszabhatóság </h3>
        <ul>
          <li> saját mélyégobjektumok, tájképek, csillagképábrák, parancsfájlok stb. hozzáadása </li>
        </ul>
