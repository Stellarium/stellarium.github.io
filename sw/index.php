        <h2> hulka </h2>
        <p><span id="version">katika toleo 0.9.0</span></p>
        <h3> mbingu </h3>
        <ul>
          <li> maelezi ya nyota zaidi ya 600,000 </li>
          <li> maelezi mengine za nyota zaidi ya milioni 210</li>
          <li> vielelezo vya mikusanyiko ya nyota </li>
          <li> picha za nebulae (orodha nzima ya Messier)</li>
          <li> Muigo-kweli wa Milky Way </li>
          <li> Muigo kweli wa maanga, macheo na machweo</li>
          <li> sayari na vimwezi vyao </li>
        </ul>
        <h3> sura </h3>
        <ul>
          <li> ulengaji hodari </li>
          <li> usukani wa wakati</li>
          <li> sura ya lugha mbalimbali </li>
          <li> uwezo wa kurekodi na kuonyesha maonyesho yako</li>
          <li> vionyesho katika jicho la samaki za kuba za usayaria</li>
          <li> vionyesho kibiringo vya kuba yako</li>
          <li> sura ya kielelezo na usukani kamili kutumia bodi ya vitufe </li>
          <li> usukani wa darubini</li>
        </ul>
        <h3> vielelezo macho </h3>
        <ul>
          <li> jedwali za istiwai na za marejeo </li>
          <li> nyota zikimetameta </li>
          <li> vimondo </li>
          <li> miigo ya kupatwa kwa mwezi au dunia </li>
          <li> mandhari, sasa na maonyesho vibiringo</li>
        </ul>
        <h3> pambo </h3>
        <ul>
          <li> ongeza vipengele vyako katika mbingu, mandhari, mikusanyiko ya nyota picha, hati... </li>
        </ul>
