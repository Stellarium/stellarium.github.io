      <div id="features">
        <h2> 機能 </h2>
        <p><span id="version">バージョン0.9.0</span></p>
        <h3> 天体 </h3>
        <ul>
          <li> 標準で60万個以上の恒星情報を収録 </li>
          <li> さらに2億1千万個以上の恒星情報も追加可能 </li>
          <li> さまざまな星座の絵</li>
          <li> 全メシエ天体を含む星雲・銀河の画像 </li>
          <li> 本物のような天の川の表示 </li>
          <li> 大気の効果を考慮した、本物のような日の出や日没 </li>
          <li> 惑星やその衛星 </li>
        </ul>
        <h3> 操作画面 </h3>
        <ul>
          <li> 便利な拡大・縮小機能 </li>
          <li> 時間の操作 </li>
          <li> 多言語対応 </li>
          <li> 操作を記録・再生できるスクリプト機能 </li>
          <li> プラネタリウムで利用できる描画機能 </li>
          <li> 自前のドームに投影するのに便利な描画設定 </li>
          <li> 画面とキーボードの両方を利用できる操作方法 </li>
          <li> 望遠鏡の操作 </li>
        </ul>
        <h3> 視覚効果 </h3>
        <ul>
          <li> 赤道座標軸と経緯座標軸の表示 </li>
          <li> 星のまたたき </li>
          <li> 流れ星の表示 </li>
          <li> 日食のシミュレーション </li>
          <li> 自由に変更できる背景画像 </li>
        </ul>
        <h3> カスタマイズ性 </h3>
        <ul>
          <li> 天体や背景、星座絵、スクリプトなどを追加可能 </li>
        </ul>
