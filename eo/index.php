      <div id="features">
        <h2> Funkcioj </h2>
        <p><span id="version">en versio 0.9.0</span></p>
        <h3> &#265;ielo </h3>
        <ul>
          <li> defa&#365;lta katalogo enhavanta &#265;irka&#365; 600,000 stelojn </li>
          <li> aldonitaj katalogoj enhavantaj pli ol 210 milionojn da 
            steloj </li>
          <li> reprezentado de stelfiguroj </li>
          <li> bildoj de nebulozoj (kompleta Messier-katalogo) </li>
          <li> realisma Lakta Vojo </li>
          <li> realismega atmosfero, sun-levi&#285;o kaj sun-subiro </li>
          <li> la planedoj kaj iliaj satelitoj </li>
        </ul>
        <h3> interfaco </h3>
        <ul>
          <li> potenca zomo </li>
          <li> tempo-kontrolo </li>
          <li> multlingva interfaco </li>
          <li> skriptado por rekordi kaj spekti viajn proprajn 
            animaciojn </li>
          <li> fi&#349;okula projekcio por planetariaj kupoloj</li>
          <li> sfer-spegula projekcio por via propra planetario </li>
          <li> grafika interfaco kaj ampleksa perklavara kontrolo </li>
          <li> kontrolo de teleskopo</li>
        </ul>
        <h3> vizualigo </h3>
        <ul>
          <li> krado ekvatora kaj azimuta </li>
          <li> stela trembrilo </li>
          <li> falsteloj </li>
          <li> simulado de eklipsoj </li>
          <li> agordeblaj pejza&#285;oj, nun videblaj per sfera projekcio</li>
        </ul>
        <h3> agordebla </h3>
        <ul>
          <li> aldonu viajn memfaritajn kosmajn objektojn, pejza&#285;ojn, 
            stelfigurajn bildojn, skriptojn... </li>
        </ul>
      </div>
