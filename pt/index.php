      <div id="features">
        <h2> recursos </h2>
        <p><span id="version">na versão 0.10.0</span></p>
        <h3> céu </h3>
        <ul>
          <li> catálogo padrão de mais de 600.000 estrelas </li>
          <li> catálogo extra com mais de 210 milhões de estrelas </li>
          <li> asterisms e ilustrações das constelações </li>
          <li> constelações de onze diferentes culturas </li>
          <li> imagens de nebulosas (catálogo Messier completo)</li>
          <li> Via Láctea realista </li>
          <li> atmosfera, nascer e pôr-do-sol bastante realistas </li>
          <li> os planetas e seus satélites </li>
        </ul>
        <h3> interface </h3>
        <ul>
          <li> um zoom poderoso </li>
          <li> controle de tempo </li>
          <li> interface em diversos idiomas </li>
          <li> projeção olho-de-peixe para redomas de planetários </li>
          <li> projeção esférica-espelhada para sua própria redoma de baixo custo </li>
          <li> nova interface gráfica e controles de teclado extensíveis </li>
          <li> controle de telescópios </li>
        </ul>
        <h3> visualização </h3>
        <ul>
          <li> grades equatorial e azimutal </li>
          <li> estrelas cintilantes </li>
          <li> estrelas cadentes </li>
          <li> simulação de eclipses </li>
          <li> terrenos personalizáveis, com projeções panorâmica e esférica </li>
        </ul>
        <h3> customização </h3>
        <ul>
          <li> adicione seus próprios objetos do céu profundo, terrenos, imagens de constelação, scripts... </li>
        </ul>
      </div>
