      <div id="features">
        <h2> ominaisuudet </h2>
        <p><span id="version">versiossa 0.9.0</span></p>
        <h3> taivas </h3>
        <ul>
          <li>oletusluettelossa yli 600 000 tähteä </li>
          <li>valinnaisesti ladattavissa yli 210 miljoonan tähden luettelo </li>
          <li>tähtikuviot ja tähdistöjen kuvitukset </li>
          <li>kuvia tähtisumuista (koko Messier'n luettelo)</li>
          <li>realistinen Linnunrata </li>
          <li>erittäin realistinen ilmakehä, auringon nousu ja lasku </li>
          <li>planeetat ja niiden luonnolliset satelliitit</li>
        </ul>
        <h3> käyttöliittymä </h3>
        <ul>
          <li>tehokas suurennustoiminto </li>
          <li>helppo ajassa liikkuminen </li>
          <li>useankielisiä esityksiä tukeva käyttöliittymä </li>
          <li>omien esitysten toistaminen ja nauhoittaminen komentosarjoilla </li>
          <li>kalansilmäprojektio puolipallo-planetaarioille </li>
          <li>pallomainen peiliprojektio omalle puolipallollesi </li>
          <li>graafinen käyttöliittymä ja kattavat näppäinkomennot </li>
          <li>kaukoputken hallinta</li>
        </ul>
        <h3> visualisaatiot </h3>
        <ul>
          <li>ekvatoriaali- ja atsimuuttiasteikot </li>
          <li>tähtien tuikkiminen </li>
          <li>tähden lennot </li>
          <li>pimennyssimulaatiot </li>
          <li>muokattavat maisemat tukevat nyt pallomaista panoraamaprojektiota</li>
        </ul>
        <h3> muokkausmahdollisuudet </h3>
        <ul>
          <li> lisää omia syvän taivaan kohteita, maisemia, tähdistökuvia, komentosarjoja...</li>
        </ul>
