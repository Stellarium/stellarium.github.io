      <div id="features">
        <h2> kenmerken </h2>
        <p><span id="version">in versie 0.10.0</span></p>
        <h3> hemel </h3>
        <ul>
          <li> standaard meer dan 600,000 sterren</li>
          <li> uitbreidingen tot 210 miljoen sterren</li>
          <li> asterismen en illustraties van de sterrenbeelden</li>
          <li> fotos van deep sky objecten (volledige Messier-catalogus)</li>
          <li> realistische Melkweg </li>
          <li> erg realistische atmosfeer, zonsopgang en zonsondergang</li>
          <li> de planeten en hun satellieten</li>
        </ul>
        <h3> interface </h3>
        <ul>
          <li> een krachtige zoom </li>
          <li> controleer de tijd </li>
          <li> scripts die toelaten je eigen show op te nemen en af te spelen </li>
          <li> visoogprojectie voor planetariumkoepels</li>
          <li> projectie voor je eigen planetarium met een sferische spiegel</li>
          <li> grafische interface en uitgebreide toetsenbordcommando's </li>
          <li> telescoopbediening</li>
        </ul>
        <h3> weergave </h3>
        <ul>
          <li> equatoriale and azimutale rasters </li>
          <li> fonkelende sterren </li>
          <li> meteoren </li>
          <li> eclipssimulatie </li>
          <li> aanpasbare landschappen, met o.a. sferische panoramaprojectie</li>
        </ul>
        <h3> aanpasbaarheid </h3>
        <ul>
          <li> voeg je eigen deep sky objecten toe, je eigen landschap, sterrenbeelden, scripts... </li>
        </ul>
      </div>
