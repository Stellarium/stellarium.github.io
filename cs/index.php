      <div id="features">
        <h2> charakterisitka </h2>
        <p><span id="version">verze 0.9.0</span></p>
        <h3> obloha </h3>
        <ul>
          <li> základní katalog obsahuje více jak 600,000 hvězd </li>
          <li> dodatečné katalogy obsahují přes 210 miliónů hvězd </li>
          <li> souhvězdí s ilustracemi</li>
          <li> fotografie mlhovin (celý Messierův katalog)</li>
          <li> realistická Mléčná dráha </li>
          <li> velmi realistická atmosféra, východ a západ slunce </li>
          <li> planety a jejich satelity </li>
        </ul>
        <h3> rozhraní </h3>
        <ul>
          <li> výkonný zoom </li>
          <li> ovládání času</li>
          <li> vícejazyčné rozhraní </li>
          <li> nahrávání a přehrávání vlastních skriptů </li>
          <li> "rybí oko" pro projekce v planetární kopuli</li>
          <li> kulová zrcadlová projekce pro vaši vlastní kopuli</li>
          <li> grafické rozhraní a mnoho klávesových zkratek </li>
          <li> ovládání dalekohledu</li>
        </ul>
        <h3> zobrazení </h3>
        <ul>
          <li> rovníkové a azimutární souřadnice </li>
          <li> mihotání hvězd </li>
          <li>meteory</li>
          <li> simulace zatmění </li>
          <li>měnitelná krajina s kulovou panoramatickou projekcí</li>
        </ul>
        <h3> uživatelské přizpůsobení </h3>
        <ul>
          <li> přidejte si svoje vlastní deep sky objekty, krajiny, obrázky souhvězdí, skripty...</li>
        </ul>
      </div>
