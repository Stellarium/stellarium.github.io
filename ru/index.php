        <h2> новости </h2>
        <p><span class="newsdate">2010/12/21</span><br>
        <span class="newstitle">Требуются волонтёры</span><br>
        <p>Нам требуются волонтёры &mdash; программировать не требуется. Нам необходим кто-то для
        координации перевода главной страницы сайта. Работа периодическая и заключается в рассылке 
        переводчикам заданий, сборе всех переводов и передаче их команде разработчиков.
        <p>Если вы можете помочь нам с этим, то <a href="http://tinyurl.com/2e9hce3">присоединяйтесь к рассылке stellarium-pubdevel</a> и 
        представьте себя.
        
	<p><span class="newsdate">2010/12/15</span><br>
	<span class="newstitle">Stellarium 0.10.6: Краткий путеводитель</span><br>
	<p>Для русскоговорящих пользователей <a href="http://astro.uni-altai.ru/~aw/stellarium/stellarium-0.10.6-short-guide.pdf">выпущен краткий путеводитель</a>, основанный на версии 0.10.6 Stellarium'а и ориентированный на новичков.</p>
	</p>
        
        
	<p><span class="newsdate">2010/12/06</span><br>
	<span class="newstitle">Обновлен windows-пакет (0.10.6.1)</span><br>
	<p>Небольшое изменение было сделано в Windows-версии, которое [мы надеемся] исправит некоторые проблемы со шрифтами у ряда людей.</p>
	</p>

	<p><span class="newsdate">2010/12/05</span><br>
  	<span class="newstitle">Stellarium 0.10.6 вышел!</span><br>
        <p>Команда разработчиков с гордостью объявляет о выпуске версии 0.10.6 Stellarium'а.</p>
        <p>Этот релиз привносит некоторые интересные новые функции:<ul>
	<li>Новая функция для установки ландшафтов из ZIP-архивов.</li>
	<li>Новый плагин: Редактор Солнечной системы.</li>
	<li>Новый плагин: Часовой пояс.</li>
	<li>Плагин Окуляры: настраиваемые горячие клавиши.</li>
	<li>Плагин Спутники: добавлен новый движок расчета предсказания орбиты спутника.</li>
	<li>Плагин Спутники: теперь можно обновлять TLE из локального файла.</li>
	<li>Плагин Управления телескопом: добавлено ручное переопределение равноденствия / эпохи.</li></ul></p>
	<p>Также было исправлено большое количество ошибок и произведены некоторые улучшения производительности.</p>
	<p>Новые пользователи найдут, что некоторые плагины включены по умолчанию. На компьютерах с уже установленной предыдущей версий Stellarium'а будут использоваться старые настройки по умолчанию.</p>
	</p>
