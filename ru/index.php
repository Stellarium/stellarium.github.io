      <div id="features">
        <h2> особенности </h2>
        <p><span id="version">в версии 0.10.6</span></p>
        <h3> небосвод </h3>
        <ul>
          <li> стандартный каталог с более чем 600,000 звёзд </li>
          <li> дополнительные каталоги с более чем 210 миллионами звёзд </li>
          <li> звёздные скопления и изображения созвездий</li>
          <li> созвездия 13 мировых культур </li>
          <li> изображения туманностей (полный каталог Мессье)</li>
          <li> реалистичный Млечный Путь </li>
          <li> очень реалистичная атмосфера, закаты и рассветы </li>
          <li> планеты и их спутники </li>
        </ul>
        <h3> интерфейс </h3>
        <ul>
          <li> мощное масштабирование </li>
          <li> контроль хода времени </li>
          <li> многоязычный интерфейс </li>
          <li> проекция "рыбий глаз" для проецирования изображения на купол планетария </li>
          <li> сферическая зеркальная проекция для Вашего личного низкобюджетного купола </li>
          <li> новый графический интерфейс и развитая система управления с клавиатуры </li>
          <li> управление телескопом </li>
        </ul>
        <h3> отображение </h3>
        <ul>
          <li> экваториальная и азимутальная сетки </li>
          <li> мерцание звёзд </li>
          <li> метеоры </li>
          <li> симуляция затмений </li>
          <li> ландшафты с поддержкой скинов, теперь со сферической панорамной проекцией </li>
        </ul>
        <h3> настраиваемость и расширение </h3>
        <ul>
 	  <li> система плагинов добавляет показ искусственных спутников, симуляцию окуляра, управление телескопом и много другое</li>
	  <li> возможность добавления новых объектов солнечной системы из интернета...</li>
          <li> добавление собственных космических объектов, ландшафтов, изображений созвездий, сценариев... </li>
        </ul>
