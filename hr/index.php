          <li> defaultni katalog sa preko 600,000 zvijezda </li>
          <li> dodatni katalozi sa više od 210 milijuna zvijezda</li>
          <li> asterizmi i ilustracije sazviježđa </li>
          <li> slike maglica (kompletni Messier katalog)</li>
          <li> realistična Mliječna staza</li>
          <li> vrlo realistična atmosfera, izlazak i zalazak sunca</li>
          <li> planeti i sateliti</li>
        </ul>
        <h3> sučelje </h3>
        <ul>
          <li> snažan zoom</li>
          <li> kontrola protoka vremena</li>
          <li>višejezično sučelje</li>
          <li> snimanje skripti za vlastite projekcije</li>
          <li>ribljooka projekcija za planetarijske kupole</li>
          <li> sferična zrcalna projekcija za vlastitu kupolu</li>
          <li> grafičko sučelje i opsežna kontrola tipkovnicom</li>
          <li> kontrola teleskopom</li>
        </ul>
        <h3> vizualizacija </h3>
        <ul>
          <li> ekvatorijalna i azimutska mreža</li>
