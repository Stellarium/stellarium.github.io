      <div id="features">
        <h2>特色</h2>
        <p><span id="version">0.8.1版</span></p>
        <h3>夜空</h3>
        <ul>
          <li>超過120000個來自Hipparcos星表的天體</li>
          <li>在不同文化的星座、星名，以及星座繪之間切換</li>
          <li>真實星雲影像</li>
          <li>真實的銀河效果</li>
          <li>非常真實的大氣和光線效果</li>
          <li>九大行星與其衛星</li>
        </ul>
        <h3>介面</h3>
        <ul>
          <li>強大的縮放功能</li>
          <li>自由控制時間流動</li>
          <li>支援多種顯示語言</li>
          <li>利用自定劇本檔演出獨特天體秀</li>
          <li>魚眼投影模式</li>
          <li>個人天頂劇場專用球面鏡像投影模式</li>
          <li>人性化的圖形介面以及便利的鍵盤操作</li>
          <li>望遠鏡控制功能</li>
        </ul>
        <h3>特效</h3>
        <ul>
          <li>赤道/地平座標網格</li>
          <li>星光閃爍效果</li>
          <li>流星</li>
          <li>日/月食模擬</li>
          <li>地面風景更換</li>
        </ul>
        <h3>自訂</h3>
        <ul>
          <li>天體、背景、星座繪、劇本......</li>
        </ul>
