        <h2> caracter&iacute;stiques </h2>
        <p><span id="version">a la versi&oacute; 0.8.2</span></p>
        <h3> cel </h3>
        <ul>
          <li> m&eacute;s de 120,000 estels del cat&agrave;leg Hipparcos amb informaci&oacute; adicional</li>
          <li> representacions de les constel&middot;lacions </li>
          <li> imatges de nebuloses </li>
          <li> Via L&agrave;ctia realista </li>
          <li> atmosfera molt real, sortida i posta de sol </li>
          <li> planetes i els seus sat&egrave;l&middot;lits </li>
        </ul>
        <h3> interf&iacute;cie </h3>
        <ul>
          <li> zoom potent</li>
          <li> control del temps</li>
          <li> interf&iacute;cie multiling&uuml;e </li>
          <li> scripts per gravar i reproduir les teves pr&ograve;pies animacions </li>
          <li> projecci&oacute; tipus fish-eye per c&uacute;pula de planetarium</li>
          <li> projecci&oacute; de tipus mirall esf&egrave;ric per fer el teu propi planet&agrave;rium </li>
          <li> interf&iacute;cie gr&agrave;fica i control complert desde el teclat</li>
          <li> control per telescopi </li>
        </ul>
        <h3> visualitzaci&oacute; </h3>
        <ul>
          <li> graelles equatorial i azimutal </li>
          <li> estels pampalluguejants </li>
          <li> estels fuga&ccedil;os </li>
          <li> simulaci&oacute; d'eclipsis </li>
          <li> paisatges personalitzables, ara amb projecci&oacute; panor&agrave;mica esf&egrave;rica</li>
        </ul>
        <h3> personaltizaci&oacute; </h3>
        <ul>
          <li> afegeix els teus propis objectes del cel profund, paisatges, imatges de constel&middot;lacions, scripts... </li>
        </ul>
