      <div id="features">
        <H2> përmbajtja </H2>
        <P><SPAN id="version">në versionin 0.10.0</SPAN></P>
        <H3> qielli </H3>
        <UL>
          <LI> katalog i parazgjedhur i më tepër se 600,000 yjeve </LI>
          <LI> katalog shtesë me më shumë se 210 milion yje </LI>
          <LI> asterizma dhe ilustrime të yjësive </LI>
          <LI> yjësi të njëmbëdhjetë kulturave të ndryshme </LI>
          <LI> pamje nebulozash (katalogu i plotë Messier)</LI>
          <LI> Udhë Qumështi si e vërtetë </LI>
          <LI> atmosferë gati e vërtetë, lindje dhe perëndim dielli </LI>
          <LI> planetët dhe satelitët e tyre </LI>
        </UL>
        <H3> ndërfaqja </H3>
        <UL>
          <LI> një zmadhim i fuqishëm </LI>
          <LI> kontroll kohe </LI>
          <LI> ndërfaqje shumëgjuhëshe </LI>
          <LI> projeksion sypeshku për kupola planetarësh </LI>
          <LI> projeksion pasqyre sferike për kupolën tuaj pa shpenzime </LI>
          <LI> ndërfaqe grafike e gjitha e re dhe kontroll i zgjeruar tastiere </LI>
          <LI> kontroll teleskopi </LI>
        </UL>
        <H3> shfaqja </H3>
        <UL>
          <LI> rrjeta ekuatoriale dhe azimutale </LI>
          <LI> vezullim yjesh </LI>
          <LI> yje që bien </LI>
          <LI> simulim eklipsi </LI>
          <LI> panorama të ndryshme, tani me projeksion panorame sferike </LI>
        </UL>
        <H3> rregullueshmëria </H3>
        <UL>
          <LI> shto objektet e tua të qiellit, panoramat, imazhet e yjësive, skripte.. </LI>
        </UL>
