      <div id="features">
        <h2> cechy programu </h2>
        <p><span id="version">wersja 0.10.0</span></p>
        <h3> niebo </h3>
        <ul>
          <li> podstawowy katalog obejmujący ponad 600 000 gwiazd </li>
          <li> dodatkowe katalogi zawierające ponad 210 milionów gwiazd </li>
          <li> asteryzmy i ilustracje gwiazdozbiorów </li>
          <li> gwiazdozbiory jedenastu różnych kultur </li>
          <li> zdjęcia mgławic (pełny katalog Messiera)</li>
          <li> realistyczna Droga Mleczna </li>
          <li> bardzo realistyczna atmosfera; wschody i&nbsp;zachody Słońca </li>
          <li> planety i ich księżyce </li>
        </ul>
        <h3> interfejs </h3>
        <ul>
          <li> rozbudowany zoom </li>
          <li> kontrola upływu czasu </li>
          <li> wielojęzyczny interfejs </li>
          <li> tryb "rybiego oka" - widok jak na kopułach planetariów </li>
          <li> projekcja zwierciadła sferycznego - stwórz swoją własną, tanią kopułę </li>
          <li> całkowicie przebudowany interfejs graficzny, rozszerzone sterowanie klawiaturą </li>
          <li> kontrola teleskopu </li>
        </ul>
        <h3> obrazowanie </h3>
        <ul>
          <li> siatki równikowe i azymutalne </li>
          <li> mrugające gwiazdy </li>
          <li> spadające gwiazdy </li>
          <li> symulacja zaćmień </li>
          <li> modyfikowalne krajobrazy (teraz z panoramą sferyczną) </li>
        </ul>
        <h3> dostosowywanie </h3>
        <ul>
          <li> twórz własne obiekty nieba, krajobrazy, ilustracje gwiazdozbiorów, skrypty... </li>
        </ul>
      </div>

