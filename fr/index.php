        <h2> fonctionnalit&eacute;s </h2>
        <p><span id="version">dans la version 0.9.0</span></p>
        <h3>Ciel </h3>
        <ul>
          <li>Catalogue de base de plus de 600 000 étoiles</li>
		  <li>Catalogues supplémentaires avec plus de 210 millions d'étoiles</li>
          <li>repr&eacute;sentation des constellations </li>
          <li>Images des objets du ciel profond (catalogue Messier complet)</li>
          <li>Voie Lact&eacute;e r&eacute;aliste </li>
          <li>atmosph&egrave;re, lev&eacute;s et couch&eacute;s de soleil ultra r&eacute;alistes </li>
          <li>les plan&egrave;tes et leurs satellites </li>
        </ul>
        <h3>Interface </h3>
        <ul>
          <li>un zoom puissant </li>
          <li>contr&ocirc;le du temps</li>
          <li>interface multi-langue </li>
          <li>scripts permettant d'enregistrer et rejouer ses propres animations </li>
          <li>projection type fish-eye pour les plan&eacute;tariums</li>
          <li>projection type miroir sph&eacute;rique pour se faire son propre plan&eacute;tarium </li>
          <li>interface graphique et contr&ocirc;le complet du logiciel avec le clavier </li>
          <li>contr&ocirc;le de t&eacute;lescope</li>
        </ul>
        <h3>Visualisation </h3>
        <ul>
          <li>grilles &eacute;quatoriale et azimutale </li>
          <li>scintillement des &eacute;toiles </li>
          <li>&eacute;toiles filantes </li>
          <li>simulation d'&eacute;clipses </li>
          <li>paysage de fond modifiable, avec un panorama en projection sph&eacute;rique</li>
        </ul>
        <h3>Adaptabilit&eacute; </h3>
        <ul>
          <li> ajouter vos propres objets du ciel profond, paysages, images de constellation,&nbsp;scripts... </li>
        </ul>
