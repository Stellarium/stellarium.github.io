      <div id="features">
         <h2> Merkmale </h2>
        <p><span id="version">in Version 0.10.2</span></p>
        <h3> Himmel </h3>
        <ul>
          <li> Standardkatalog mit mehr als 600.000 Sternen </li>
          <li> Zusatzkataloge mit mehr als 210 Millionen Sternen </li>
          <li> Asterismen und Illustrationen von Sternbildern </li>
          <li> Sternbilddarstellungen aus zwölf verschiedenen Kulturkreisen </li>
          <li> Bilder von Nebeln (vollständiger Messier-Katalog) </li>
          <li> Realistische Milchstraße </li>
          <li> Sehr realistische Atmosphäre, Sonnenauf- und untergang </li>
          <li> Planeten und ihre Satelliten </li>
        </ul>
        <h3> Benutzerschnittstelle </h3>
        <ul>
          <li> Leistungsfähige Vergrößerung </li>
          <li> Zeitsteuerung </li>
          <li> Mehrsprachige Benutzerschnittstelle </li>
          <li> Fischaugenprojektion für Planetariumskuppeln </li>
          <li> Sphärische Spiegelprojektion für Ihre eigene Kuppel </li>
          <li> Graphische Benutzerschnittstelle und umfassende Tastatursteuerung </li>
          <li> Teleskopsteuerung </li>
        </ul>
        <h3> Darstellung </h3>
        <ul>
          <li> Äquatoriale und azimutale Gitter </li>
          <li> Funkelnde Sterne </li>
          <li> Sternschnuppen </li>
          <li> Simulation von Sonnen- und Mondfinsternis </li>
          <li> Anpaßbare Landschaften, jetzt mit sphärischer Panoramaprojektion </li>
        </ul>
        <h3> Individualisierungsmöglichkeiten </h3>
        <ul>
          <li> Fügen Sie eigene extrasolare Objekte, Landschaften, Sternbilder, Skripte, etc. hinzu </li>
        </ul>
